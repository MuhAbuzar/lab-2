public class Variable {

    public static void main(String[] args) {
        
        // Integer types
        int age = 25;  // Age of a person
        short numberOfChildren = 2;  // Number of children in a family
        long population = 7896543210L;  // Total population of a city

        // Floating-point types
        float temperature = 98.6f;  // Body temperature in Fahrenheit
        double distance = 3567.89;  // Distance in kilometers

        // Character type
        char grade = 'A';  // Grade of a student

        // Boolean type
        boolean isStudent = true;  // Whether a person is a student or not

        // String type
        String name = "John Doe";  // Name of a person

        

        // Displaying values
        System.out.println("Age: " + age);
        System.out.println("Number of Children: " + numberOfChildren);
        System.out.println("Total Population: " + population);
        System.out.println("Body Temperature: " + temperature);
        System.out.println("Distance: " + distance);
        System.out.println("Grade: " + grade);
        System.out.println("Is Student: " + isStudent);
        System.out.println("Name: " + name);
        
    }
}
